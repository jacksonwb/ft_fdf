#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*temp;

	if (!alst || !*alst)
		return ;
	temp = *alst;
	while (temp)
	{
		del(temp->content, temp->content_size);
		*alst = temp->next;
		free(temp);
		temp = *alst;
	}
}
